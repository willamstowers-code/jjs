// AUTO AIM FUNCTION
function getNearestEnemy () {
    enemies = sprites.allOfKind(SpriteKind.Enemy)
    bestDist = 999999
    closest = null
for (let value of enemies) {
        d = Math.sqrt((value.x - player2.x) ** 2 + (value.y - player2.y) ** 2)
        if (d < bestDist) {
            bestDist = d
            closest = value
        }
    }
    return closest
}
